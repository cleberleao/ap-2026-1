//Escreva um programa que receba 4 valores em variáveis diferentes, contendo INTEIRO,
// REAL, CARACTERE e BOOLEANO e mostre na tela os valores atribuídos às variáveis
public class DeclaracaoVariaveis {
    public static void main(String[] args) {

        int numeroUm = 20; //numero inteiro
        float numeroDois = 15.5f; //numeros reais
        String nome = "Cleber"; // conjunto de caractere
        boolean vf = true; //ou false

        System.out.println("Este é meu numero inteiro: " + numeroUm);
        System.out.println("Este é meu numero real: " + numeroDois);
        System.out.println("Este é meu nome: " + nome);
        System.out.println("Este é meu valor lógico: " + vf);


    }
}
